def log(msg):
    print(msg, flush=True)


def content_tags(pos):
    return [p.strip() for p in re.split(r"[/・]", pos or "") if p.strip() in CONTENT_POS]


def is_candidate(row, protected):
    w = row["w"]
    if w in protected:
        return False
    if row.get("cefr") not in ("A1", "A2"):
        return False
    pos = row.get("pos") or ""
    if any(fp in pos for fp in FUNCTION_POS) and not content_tags(pos):
        return False
    if w in MANUAL:
        return True
    return len(content_tags(pos)) >= 2


def shorten(defn):
    text = re.sub(r"\([^)]*\)", "", defn)
    text = text.split(";")[0].split(",")[0].strip()
    text = re.sub(r"^to\s+", "", text, flags=re.I)
    return text[:48].strip()


def wordnet_senses(word, prefer_pos):
    wn_pos = [POS_TO_WN[p] for p in prefer_pos if p in POS_TO_WN]
    synsets = wn.synsets(word)
    ordered = []
    for p in wn_pos:
        ordered.extend([s for s in synsets if s.pos() == p])
    ordered.extend([s for s in synsets if s.pos() in "nvar" and s not in ordered])
    senses, seen = [], set()
    for syn in ordered:
        short = shorten(syn.definition())
        if len(short) < 3 or SKIP_RE.search(short) or len(short.split()) > 10:
            continue
        key = short.lower()
        if key in seen:
            continue
        seen.add(key)
        senses.append(short)
        if len(senses) >= 4:
            break
    return senses


def format_gloss(items, lang):
    items = [x.strip() for x in items if x and x.strip()]
    if not items:
        return ""
    if len(items) == 1:
        return items[0]
    top, overflow = items[:3], len(items) > 3
    if lang == "ja":
        return "、".join(top) + ("など" if overflow else "")
    if lang == "zh":
        return "、".join(top) + ("等" if overflow else "")
    if lang == "ko":
        return ", ".join(top) + (" 등" if overflow else "")
    return top[0]


def translate_batch(texts, tr, retries=3):
    for attempt in range(retries):
        try:
            if len(texts) == 1:
                return [tr.translate(texts[0])]
            return tr.translate_batch(texts)
        except Exception as e:
            if attempt == retries - 1:
                raise e
            time.sleep(0.5 * (attempt + 1))
    return []


def apply_manual(gloss, w):
    ja, zh, ko = MANUAL[w]
    gloss["en"] = w
    gloss["ja"] = format_gloss(ja, "ja")
    gloss["zh"] = format_gloss(zh, "zh")
    gloss["ko"] = format_gloss(ko, "ko")


def apply_clear(rows, clear):
    by_w = {r["w"]: r for r in rows}
    for w, patch in clear.items():
        if w not in by_w:
            continue
        g = by_w[w].setdefault("gloss", {})
        g["en"] = w
        for lang, val in patch.items():
            g[lang] = val


def fallback_senses(word, tags):
    senses = []
    if "動詞" in tags:
        senses.append(f"to {word}")
    if "名詞" in tags:
        senses.append(f"{word} (noun)")
    if "形容詞" in tags:
        senses.append(f"{word} (adjective)")
    if "副詞" in tags:
        senses.append(f"{word} (adverb)")
    return senses[:3]


def main():
    protected = set(json.loads(CLEAR.read_text(encoding="utf-8")).keys())
    rows = json.loads(WORDLIST.read_text(encoding="utf-8"))
    targets = [r for r in rows if is_candidate(r, protected)]
    log(f"polysemy candidates: {len(targets)}")

    tr_ja = GoogleTranslator(source="en", target="ja")
    tr_zh = GoogleTranslator(source="en", target="zh-CN")
    tr_ko = GoogleTranslator(source="en", target="ko")
    updated = 0

    for row in targets:
        w = row["w"]
        g = row.setdefault("gloss", {})
        if w in MANUAL:
            apply_manual(g, w)
            updated += 1
            continue

        tags = content_tags(row.get("pos", ""))
        senses = fallback_senses(w, tags)
        if len(senses) < 2:
            continue
        try:
            ja = translate_batch(senses[:3], tr_ja)
            time.sleep(SLEEP)
            zh = translate_batch(senses[:3], tr_zh)
            time.sleep(SLEEP)
            ko = translate_batch(senses[:3], tr_ko)
            time.sleep(SLEEP)
        except Exception as e:
            log(f"  skip {w}: {e}")
            continue

        g["en"] = w
        g["ja"] = format_gloss(ja, "ja")
        g["zh"] = format_gloss(zh, "zh")
        g["ko"] = format_gloss(ko, "ko")
        updated += 1
        if updated % 40 == 0:
            log(f"  updated {updated} …")

    apply_clear(rows, json.loads(CLEAR.read_text(encoding="utf-8")))
    WORDLIST.write_text(json.dumps(rows, ensure_ascii=False, indent=1), encoding="utf-8")
    log(f"done: updated {updated} words; clear corrections re-applied")


if __name__ == "__main__":
    main()
